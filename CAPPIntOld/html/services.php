                            <p class="text10pt" style="margin-bottom: 0px;">USPAP was originally written in 1986-87 by an ad-hoc committee representing the various appraisal professional organizations in the U.S. and Canada. The copyright to USPAP was donated to TAF in April 27,1987. While USPAP answers a specific regulatory need in the U.S., it has also been adopted by many appraisal professional organizations throughout North and South America, Europe, and Asia.<br><br>USPAP represents the generally accepted and recognized standards of appraisal practice and its organizational meeting held on January 30, 1989, the appraisal standard board unanimously approved and adopted the original USPAP as the initial appraisal standards promulgated by ASB. USPAP may be amended, Interpreted, or retired by ASB after exposure to the user&#39;s of appraisal services and the public in accordance with established rules of procedure.<br><br>Over the years, the USPAP document has evolved in content and organizational structure in response to changes in appraisal practice. The ASB has developed a process for developing both standards and guidance based, in part, on written comments submitted in response to exposure drafts and oral testimony.<br><br>While USPAP provides a minimum set of quality control standards for the conduct of appraisal in the U.S., it does not attempt to prescribe specific methods to be used. Rather, USPAP simply requires that appraisers be familiar with and correctly utilize those methods which would be acceptable to other appraisers familiar with the assignment at hand and acceptable to the intended users of the appraisal. USPAP directs this through what is called the Scope of Work rule. At the onset of an assignment, an appraiser is obligated to gather certain specified preliminary data about the project, such as the nature of the property to be appraised, the basis of value (e.g. market, investment, impaired, unimpaired), the interests appraised (e.g. fee, partial), important assumptions or hypothetical conditions, and the effective date of the valuation. Based on this and other key information, the appraiser relies on peer-reviewed methodology to formulate an acceptable work-plan.</p>
